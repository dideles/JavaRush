package com.javarush.tasks.Level_1.Task_1;

// Массив символов с обработкой

public class Solution_9 {
    public static void main(String[] args) {
        char[] message = {'А', 'м', 'и', 'г', 'о', ' ', 'о', 'ч', 'е', 'н', 'ь', ' ', 'у', 'м', 'н', 'ы', 'й'};
        for (char c : message) {
            System.out.print(c);
        }
        System.out.println();
    }
}